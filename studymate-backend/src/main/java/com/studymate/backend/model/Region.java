package com.studymate.backend.model;

/**
 * Enum representing geographic regions for study hall discovery.
 *
 * Used for filtering and organizing study halls by location zones.
 */
public enum Region {
    /**
     * North zone region.
     */
    NORTH_ZONE,

    /**
     * South zone region.
     */
    SOUTH_ZONE,

    /**
     * East zone region.
     */
    EAST_ZONE,

    /**
     * West zone region.
     */
    WEST_ZONE,

    /**
     * Central region.
     */
    CENTRAL
}
